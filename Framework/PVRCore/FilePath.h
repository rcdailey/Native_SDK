#include "PVRCore/Defines.h"
#pragma once

namespace pvr {
/*!*********************************************************************************************************************
\brief         Filepath represents a Path + Filename + Extension.
***********************************************************************************************************************/
class FilePath : public string
{
public:
	/*!*********************************************************************************************************************
	\brief     Creates an empty Filepath object.
	***********************************************************************************************************************/
	FilePath();

	/*!*********************************************************************************************************************
	\brief     Creates an Filepath object from a filename.
	***********************************************************************************************************************/
	FilePath(const string& str);

	/*!*********************************************************************************************************************
	\brief     Get a string containing the Extension of the filepath.
	***********************************************************************************************************************/
	string getFileExtension() const;

	/*!*********************************************************************************************************************
	\brief     Get a string containing the Directory of the filepath.
	***********************************************************************************************************************/
	string getDirectory() const;

	/*!*********************************************************************************************************************
	\brief     Get a string containing the Filename+Extension of the filepath.
	***********************************************************************************************************************/
	string getFilename() const;

	/*!*********************************************************************************************************************
	\brief     Get a string containing the Filename (without extension) of the filepath.
	***********************************************************************************************************************/
	string getFilenameNoExtension() const;

	/*!*********************************************************************************************************************
	\brief     Get the directory separator used by the current platform.
	***********************************************************************************************************************/
    static tchar getDirectorySeparator();

private:
	static const char8 c_unixDirectorySeparator = '/';
	static const char8 c_windowsDirectorySeparator = '\\';
	static const char8 c_extensionSeparator = '.';
};
}

